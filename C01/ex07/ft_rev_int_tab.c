void	ft_rev_int_tab(int *tab, int size)
{
	int abc;
	int i;
	int a;

	i = 0;
	a = 0;
	while (tab[a] != '\0')
	{
		a++;
	}
	while (a > i)
	{
		abc = tab[i];
		tab[i] = str[a];
		tab[a] = abc;
		i++;
		a--;
	}
}
